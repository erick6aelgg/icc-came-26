public class POO {
    public static void main(String[] args) {
        System.out.println("Bienvenido a la clase de POO");

        /**
         * La programacion orientada a objetos es una forma de pensar y organizar la solucion a un problema.
         *
         * Busca representar las entidades o conceptos importantes de un problema mendiante objetos.
         *
         * Antes de programar podemos preguntarnos:
         * Quienes participan en el problema?
         * Que informacion necesitamos guardar?
         * Que responsabilidades o comportamientos tienen?
         *
         * Para modelar estos conceptos, utilizamos la abstraccion.
         *
         * La abstraccion consiste en tomar unicamente las caractaeristicas esenciales de algo
         * segun el problema a resolver. No buscamos representar la realidad completamente, sino
         * unicamente aquello que es relevante para nuestro programa.
         *
         * **/

        /**
         * Una clase es una plantilla de codigo que define la estructura y el comportamiento de nuestros objetos.
         *
         * LA CLASE REPRESENTA LA GENERALIZACION (Eje. Clase Fruta).
         *
         * Un objeto es una instancia concreta de nuestra clase (Ej. Fresa, Manzana, etc).
         *
         * Los objetos tienen:
         * 1. ESTADO
         * Los valores actuales almacenados en sus atributos.
         *
         * 2. COMPORTAMIENTO
         * Operaciones que pueden realizar, representadas mendiante metodos.
         *
         * 3. IDENTIDAD
         * Cada objeto es una instancia independiente y puede distinguirse de otros objetos.
         * **/


    }
}
